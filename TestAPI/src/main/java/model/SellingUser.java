package model;

public class SellingUser {
    private String selling_user_id;
    private String selling_user_name;
    private String selling_user_avatar;

    public SellingUser() {
    }

    public String getSelling_user_id() {
        return selling_user_id;
    }

    public String getSelling_user_name() {
        return selling_user_name;
    }

    public String getSelling_user_avatar() {
        return selling_user_avatar;
    }

    public void setSelling_user_id(String selling_user_id) {
        this.selling_user_id = selling_user_id;
    }

    public void setSelling_user_name(String selling_user_name) {
        this.selling_user_name = selling_user_name;
    }

    public void setSelling_user_avatar(String selling_user_avatar) {
        this.selling_user_avatar = selling_user_avatar;
    }
}
